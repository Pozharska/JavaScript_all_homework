// Оператори порівняння < > <= >= == ===

console.log(5 > 10);
console.log(5 < 10);
console.log(5 <= 5);
console.log(5 >= 7);

console.log('==', 100 == 100);

//  Порівняння рядків
console.log('JaVaScript' <= 'JavaScript');
console.log('V', 'V'.charCodeAt());
console.log('v', 'v'.charCodeAt());

//  == vs. ===

//  ==  порівнює значення
//  === порівнює типи

console.log('1'== 1);
console.log('1' >= 1);
console.log('200' == '21');
console.log(true == 1);
console.log(false == 1);

// ===
console.log('1' === 1);
console.log(true === 1);

